#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	while(argc && i == 0)
	{
		ft_putstr(argv[0]);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
